package cse135;

public class Product {
	public int sku;
	public String name;
	public double price;
	public int quantity;
	public int id;
	
	public Product(int sku, String name, double price, int quantity, int id){
		this.sku = sku;
		this.name = name;
		this.price = price;
		this.quantity = quantity;
		this.id = id;
	}

}
